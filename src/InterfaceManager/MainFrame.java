    private void CarregaComboBoxes(){
    
        //traz lista de clientes do xls para a listaClientes do MainFrame
        this.listaClientes = xls.getLsCliente();
        cbCliente.removeAllItems();
        
        //la√ßo que preenche o combobox com os clientes
        for(Cliente c : listaClientes){
            
            cbCliente.addItem(c.getNomeCliente());
            
        }
        
        
        //...TipoAtividade
        this.listaTipoAtividade = xls.getLsTipoAtividade();
        cbTipoAtividade.removeAllItems();
        
        //la√ßo que preenche o combobox com os clientes
        for(TipoAtividade t : listaTipoAtividade){
            
            //cbTipoAtividade.addItem(t.getNomeTipo());
            cbTipoAtividade.addItem(t.getNomeTipo());
            
        }
        
        //...Processo
        this.listaProcesso = xls.getLsProcesso();
        cbProcesso.removeAllItems();
        
        //la√ßo que preenche o combobox com os clientes
        for(Processo p : listaProcesso){
            
            //tratamento do nome, cortando trecho "Macro process..."
            int corte = p.getNomeProcesso().indexOf(" - ")+3;
            
            if(p.getNomeProcesso().contains("Abono")){
                corte = p.getNomeProcesso().indexOf("Abono");
            }
            //cbTipoAtividade.addItem(t.getNomeTipo());
            cbProcesso.addItem(p.getNomeProcesso().substring(corte));
            
            
        }
        
        
        
    
    }
    
    
